def advanced_has_kw(target_text, category_name):
    if not target_text:
        return False
    target_text_lower = target_text.lower()
    return any(kw.lower() in target_text_lower for kw in KEYWORD_THESAURUS.get(category_name, []))

def load_stock_list(filepath):
    """Loads all tickers and names from stocks_list.txt."""
    stocks = []
    if not os.path.exists(filepath):
        print(f"Error: {filepath} not found.")
        return []
    with open(filepath, "r", encoding="utf-8", errors="ignore") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            parts = line.split(",", 1)
            if len(parts) >= 2:
                ticker = parts[0].strip().lstrip('\ufeff')
                name = parts[1].strip()
                stocks.append((name, ticker))
    return stocks

def fetch_yfinance_details(name, ticker, cache):
    """Fetches details from yfinance or cache."""
    if ticker in cache:
        return cache[ticker]
    try:
        t_obj = yf.Ticker(ticker)
        info = t_obj.info
        sector = info.get("sector")
        industry = info.get("industry")
        res = {
            "name": name,
            "ticker": ticker,
            "sector": sector if sector else "N/A",
            "industry": industry if industry else "N/A",
            "longName": info.get("longName", name),
            "longBusinessSummary": info.get("longBusinessSummary", ""),
            "status": "success"
        }
        return res
    except Exception as e:
        return {
            "name": name,
            "ticker": ticker,
            "sector": "N/A",
            "industry": "N/A",
            "status": "error",
            "error": str(e)
        }

def save_cache(cache):
    try:
        with open(CACHE_FILE, "w", encoding="utf-8") as f:
            json.dump(cache, f, ensure_ascii=False, indent=2)
    except Exception as e:
        print("Warning: Failed to save cache:", e)

